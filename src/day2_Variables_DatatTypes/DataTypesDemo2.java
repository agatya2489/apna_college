package day2_Variables_DatatTypes;

public class DataTypesDemo2 {

	public static void main(String[] args) {
//		
//		//Numeric - Integer Data Type
//		
		int a=100, b=200;
		System.out.println("int");
		System.out.println("The value of a is:"+a);
		System.out.println("The value of b is:"+b);
		System.out.println();
		
		long x=1234567890;
		System.out.println("long");
		System.out.println("The value of x is:"+x);
		System.out.println();
		
		byte c=125;
		System.out.println("byte");
		System.out.println("The value of c is:"+c);
		System.out.println();
		
		short sh=32765;
		System.out.println("short");
		System.out.println("The value of sh is:"+sh);
		System.out.println();
		
		
		//Decimal Numbers - float,double
		
		float item_price=15.50f;
		System.out.println("float");
		System.out.println(item_price);
		System.out.println();
		
		double d=1234.5678912345;
		System.out.println("double");
		System.out.println(d);
		System.out.println();
		
		double du=125;
		System.out.println(du);
		System.out.println();
		
		char grade='A';
		System.out.println("char");
		System.out.println("The grade of a student is:"+grade);
		System.out.println();
		
		boolean bo=true;
		System.out.println("boolean");
		System.out.println(bo);
		System.out.println();
		
		String person_name="John";
		System.out.println("string");
		System.out.println("The of the person is:"+person_name);
		System.out.println();
			
	}

}
